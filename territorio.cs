public class Territorio
{
    string id;
    string nome;
    string id_continente;
    string simbolo;
    string confini;
    string truppe;

    public Territorio(string Id, string Nome, string Id_continente, string Simbolo, string Confini, string Truppe)
    {
        id = Id;
        nome = Nome;
        id_continente = Id_continente;
        simbolo = Simbolo;
        confini = Confini;
        truppe = Truppe;

    }
}